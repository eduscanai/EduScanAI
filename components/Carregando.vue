<template>
  <div :class="['flex flex-col items-center justify-center', classe]">
    <svg
      :class="['animate-spin text-primary-500', tamanhoClasse]"
      xmlns="http://www.w3.org/2000/svg"
      fill="none"
      viewBox="0 0 24 24"
    >
      <circle class="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" stroke-width="4" />
      <path class="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z" />
    </svg>
    <p v-if="texto" class="text-sm text-text-secondary mt-3">{{ texto }}</p>
  </div>
</template>

<script setup lang="ts">
const props = withDefaults(defineProps<{
  texto?: string
  tamanho?: 'sm' | 'md' | 'lg'
  classe?: string
}>(), {
  tamanho: 'md',
  classe: 'py-12'
})

const tamanhoClasse = computed(() => {
  switch (props.tamanho) {
    case 'sm': return 'w-5 h-5'
    case 'lg': return 'w-10 h-10'
    default: return 'w-8 h-8'
  }
})
</script>
